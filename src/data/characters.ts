function makeCharacter(seed: CharacterSeed): CharacterMatch {
  return {
    ...seed,
    image: createCharacterArtwork({
      name: seed.name,
      title: seed.title,
      game: seed.game,
      accent: seed.accent,
    }),
    vector: {
      ...baseVectors[seed.archetypeId],
      ...seed.vector,
    },
  }
}

